		<p class="snippettext">A team and I presented a poster paper for the European Conference on Technology Enhanced Learning in 2012. The paper was on collaborative learning in a classroom environment, especially for novice programmers. </p><br />In freshman year, professor Ursula Wolz suggested a group of programmers and I to work with her on creating a learning program for the classroom setting.
<br/><br/> 
The program focused novice programmers and on the fact that, currently, many programming classes take a very individual path to learning.
We argued that a more collaborative approach would better foster learning, operating on our own experiences in teams at olin in the prior smemester.
<br/><br/>
Thus, the product that we created was part IDE, part online community. 
<br/><br/>
Completely from scratch, and in python, me and a partner were responsible for creating the front end for this project.
<br/><br/>
By the end of the year, our prototype for the program was done and we were encouraged to submit our idea to the European Conference on Technology Enhanced Learning.
