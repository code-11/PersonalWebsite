		<p class="snippettext">An early class at Olin, Design Nature, had a team and I create a nature inspired robot capable of transporting appropriate items. We made a bipedal penguin, which could carry (fake) fish in its mouth and traverse a maze.  </p><br /><img id=cad-pic src='../Pictures/5432-PENGUIN-resized.jpg'>The goal of the project was to pick and design an animatronic 'transporter' which had the following requirements/restrictions:<br />
</p>
	<ul>
		<li>It had to look and move like an animal</li>
		<li>It had to carry an appropriate load</li>
		<li>It had to be child friendly</li>
		<li>The transporter also had to come with instructions to a game that a child could play</li>
	</ul>
<p>

<iframe class="no-side-margin" width="400" height="315" src="//www.youtube.com/embed/bXkNut9oV_A" frameborder="0" allowfullscreen></iframe>

One aspect of the penguin which we specifically wanted to preserve was its stereotypical waddle. This meant preserving the penguin's bipedal nature, a challenging task!
<br /><br/>

<img id=float-pic-left src='../Pictures/5417-side-resized.jpg'>
 We worked hard on the design of the penguin and analyzed videos on penguin movement and behavorial documentaries in order to replicate this waddle.
<br /><br />
In the end we had sucessfully created a penguin, using lasercut plastic and fabric skin that could walk in all directions and open and close its mouth to receive fish.

The penguin was controlled via remote control and had to navigate though a snowy themed maze with its fish in order to deposit the fish in its nest.




